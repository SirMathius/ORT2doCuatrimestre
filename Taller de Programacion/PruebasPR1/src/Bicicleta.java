
public class Bicicleta {

	private String marca;
	private String modelo;
	private int cantKilometros;

	public Bicicleta(String marca, String modelo, int cantKilometros) {
		super();
		this.marca = marca;
		this.modelo = modelo;
		this.cantKilometros = cantKilometros;
	}

	public int getCantKilometros() {
		return cantKilometros;
	}


	
}
